public class Patterna
{
    public static void main()
    {
        for(int i = 1; i<=3; i++)
        {
            for(int j = 1; j<=5; j++)
            {
                System.out.print("@ ");
            }
            System.out.println();
        }
    }
    public static void ma()
    {
        for(int i = 1; i<=4; i++)
        {
            for(int j = 1; j<=4; j++)
            {
                System.out.print("$ ");
            }
            System.out.println();
        }
    }
}